package org.firstinspires.ftc.teamcode.mybot;

public class MotionProfile {
    public final double vMin;
    public final double vMax;
    public final double accel;

    public MotionProfile(double vMin, double vMax, double accel){
        this.vMin = vMin;
        this.vMax = vMax;
        this.accel = accel;
    }
}
